    [Flags]
    public enum KWP2000DTCStatus : byte
    {
		//I think the user may only need to see the messages for bits that are set. Need to double check the flow chart to investigate more.

        [ValueMappedDescriptionAttribute(true, "Pending fault currently present")]
        [ValueMappedDescriptionAttribute(false, "Pending fault not currently present")]
        PendingFaultPresent = 0x01,//This bit indicates that the fault indicated by this DTC is currently present in the server (ECU).
        [ValueMappedDescriptionAttribute(true, "Pending fault was present during the current driving cycle")]
        [ValueMappedDescriptionAttribute(false, "Pending fault was not present during this driving cycle")]
        PendingFaultState = 0x02,//This bit indicates that the pending fault indicated by this DTC has been present (bit 0 has been set) at least once during the current driving cycle.
        [ValueMappedDescriptionAttribute(true, "Fault test is running")]
        [ValueMappedDescriptionAttribute(false, "Fault test is not running")]
        TestRunning = 0x04,//This bit indicates whether the diagnostic trouble code test conditions are met at the time of request.
        [ValueMappedDescriptionAttribute(true, "Fault test is currently inhibited due to another fault being present")]
        [ValueMappedDescriptionAttribute(false, "Fault test is not currently inhibited by another fault being present")]
        TestInhibit = 0x08,//This bit indicates that the diagnostic trouble code test conditions are not met because of another fault in the server (ECU).
        [ValueMappedDescriptionAttribute(true, "Fault test has not been completed")]
        [ValueMappedDescriptionAttribute(false, "Fault test has been completed")]
        TestReadiness = 0x10,//This bit indicates that the diagnostic trouble code test has not yet been completed during the current driving cycle.
        [ValueMappedDescriptionAttribute(true, "Fault has been validated and stored")]
        [ValueMappedDescriptionAttribute(false, "Fault has not been validated and not stored")]
        DTCStorageState = 0x20,//This bit indicates that the fault indicated by this DTC has been validated (bit 6 has been set) at least once and stored in non volatile memory since this DTC was last cleared.
        [ValueMappedDescriptionAttribute(true, "Fault currently validated")]
        [ValueMappedDescriptionAttribute(false, "No validated fault present")]
        ValidatedFaultPresent = 0x40,//This bit indicates that the fault indicated by this DTC is currently validated in the server (ECU).
        [ValueMappedDescriptionAttribute(true, "Fault was validated during the current driving cycle")]
        [ValueMappedDescriptionAttribute(false, "Fault has not been validated during current driving cycle")]
        ValidatedFaultState = 0x80//This bit indicates that the fault indicated by this DTC has been validated (bit 6 has been set) at least once during the current driving cycle.
    };

